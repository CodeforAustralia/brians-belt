<h3 class="a_center">Community Corrections Order</h3>

<div>
	<p>Not commit a further offence</p>
</div>
<div>
	<p>Comply with any obligation or requirement prescribed by regulation 17 of the Sentencing Regulations 2011</p>
</div>
<div>
	<p>Report within 2 business days of commencing the order </p>
</div>
<div>
	<p>Attend regular and ongoing supervision appointments as required</p>
</div>
<div>
	<p>Notify any change of address or employment within 2 business days</p>
</div>
<div>
	<p>Not leave the State of Victoria without the permission of your Case Manager</p>
</div>
<div>
	<p>Obey all lawful instructions and directions from your Case Manager which may be written or verbal</p>
</div>

<h3 class="a_center">Parole Order</h3>

<div>
	<p>You must not break any law </p>
</div>
<div>
	<p>You must report to the community corrections centre within 2 clear working days after the Order comes into force </p>
</div>
<div>
	<p>You are under supervision of a community corrections officer</p>
</div>
<div>
	<p>You must report to, and receive visits from, a community corrections officer as and when directed</p>
</div>
<div>
	<p> You must notify a community corrections officer of any change of address at least 2 clear working days <strong>before</strong> the change of address</p>
</div>
<div>
	<p>You must notify a community corrections officer of any change of employment within 2 clear working days of the change of employment</p>
</div>
<div>
	<p>You must not leave Victoria without the written permission of the Regional Manager</p>
</div>
<div>
	<p>You must comply with any direction given by a community corrections officer or the Regional Manager or the APB that is necessary to give to ensure that you comply with the Order</p>
</div>
<div>
	<p>You must be available for interview by a community corrections officer, the Regional Manager or the APB at the time and place as directed</p>
</div>
<div>
	<p>You must attend in person at a community corrections centre as directed in writing by a community corrections officer</p>
</div>